
public class Exam1 {
	public static void main(String[] args) {
		// 변수의 선언 : 변수만들기
		int num1;
		// 변수의 데이터 저장
		num1 = 100;
		
		// 저장된 데이터 확인
		System.out.println(num1);
		// 선언 + 저장
		long num2 = 1234567890123456789L;  // L : long형 정수를 나타냄
		System.out.println(num2);
		
		byte t = 100;
		System.out.println(t);
		
		Short s = 250;
		System.out.println(s);
		
		char c = '가';
		System.out.println(c);
		
		boolean b = true;
		System.out.println(b);
		
		double num3 = 3.14;		// 8byte 실수
		System.out.println(num3);
		
		float num4 = 3.14F;		// 4byte 실수
		System.out.println(num4);
	}
}
